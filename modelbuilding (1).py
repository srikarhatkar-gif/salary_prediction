print(f"{'Support Vector Regressor':<27} {svr_mae:<10.2f} {svr_mse:<15.2f} {svr_rmse:<15.2f} {svr_r2:<15.2f}")

import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd

# Create a DataFrame for easy plotting of metrics
model_names = ['Decision Tree Regressor', 'Random Forest Regressor', 'Linear Regression', 'Support Vector Regressor']
mae_scores = [dt_mae, rf_mae, lr_mae, svr_mae]
mse_scores = [dt_mse, rf_mse, lr_mse, svr_mse]
rmse_scores = [dt_rmse, rf_rmse, lr_rmse, svr_rmse]
r2_scores = [dt_r2, rf_r2, lr_r2, svr_r2]

performance_df = pd.DataFrame({
    'Model': model_names,
    'MAE': mae_scores,
    'MSE': mse_scores,
    'RMSE': rmse_scores,
    'R-squared': r2_scores
})

performance_df_sorted = performance_df.sort_values(by='R-squared', ascending=False)

# Set up the matplotlib figure and axes
fig, axes = plt.subplots(3, 1, figsize=(12, 18))
fig.suptitle('Model Performance Comparison', fontsize=16)

# Bar plot for R-squared
sns.barplot(x='R-squared', y='Model', data=performance_df_sorted, palette='viridis', ax=axes[0])
axes[0].set_title('R-squared Scores (Higher is Better)')
axes[0].set_xlabel('R-squared')
axes[0].set_ylabel('')
axes[0].set_xlim(0, 1) # R-squared typically ranges from 0 to 1

# Bar plot for MAE
sns.barplot(x='MAE', y='Model', data=performance_df_sorted, palette='magma', ax=axes[1])
axes[1].set_title('Mean Absolute Error (MAE) (Lower is Better)')
axes[1].set_xlabel('MAE')
axes[1].set_ylabel('')

# Bar plot for RMSE
sns.barplot(x='RMSE', y='Model', data=performance_df_sorted, palette='cividis', ax=axes[2])
axes[2].set_title('Root Mean Squared Error (RMSE) (Lower is Better)')
axes[2].set_xlabel('RMSE')
axes[2].set_ylabel('')

plt.tight_layout(rect=[0, 0.03, 1, 0.96]) # Adjust layout to prevent title overlap
plt.show()

